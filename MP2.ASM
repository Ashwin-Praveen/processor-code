	STR DB 80 DUP(0) 				;ARRAY 

.CODE							;START OF CODE SEGMENT

	MOV AX,@DATA
	MOV DS,AX
	LEA DX,OFFSET MSG
	MOV AH,09H					;DISPALY STRING 
	INT 21H						;INTERRUPT
	MOV SI,OFFSET STR
	MOC CX,00H					;CX IS SET TO ZERO 
		
 BACK:
	READ_CHAR					;MACRO CALL FROM THE LINKED FILE
	MOV [SI],AL					;READ VALUE WILL BE STORED IN AL
	INC SI						;GONG TO THE NEXT INDEX
	INC CX						;VALUE STORES THE TOTAL LENGTH OF THE STRING
	CMP AL,0DH					;CHECKS OF [ENTER] IS PRESSED
	JNE BACK
	MOV SI,OFFSET STR
	
 BACK1:
	MOV DL,[SI]					;IT HAS TO BE TRANSFERED TO DL FOR THE 02H OPERATION
	DISPLAY_CHAR					;MACRO FROM THE LINKED FILE
	INC SI
	LOOP BACK1					;LOOPS FORTHE LENGTH OG THE STRING
	JMP EXIT					;EXITS

 EXIT:
	MOV AH,4CH					;TERMINATION OF THE CODE
	INT 21H					
	END						;END OF THE PROGRAM
	
